package com.bean;
public class ClazzBean {

    private int Clazz_ID ;
    private String Clazz_Name ;
    private String Clazz_Introduction ;
	public int getClazz_ID() {
		return Clazz_ID;
	}
	public void setClazz_ID(int clazzID) {
		Clazz_ID = clazzID;
	}
	public String getClazz_Name() {
		return Clazz_Name;
	}
	public void setClazz_Name(String clazzName) {
		Clazz_Name = clazzName;
	}
	public String getClazz_Introduction() {
		return Clazz_Introduction;
	}
	public void setClazz_Introduction(String clazzIntroduction) {
		Clazz_Introduction = clazzIntroduction;
	}
    
	
}
